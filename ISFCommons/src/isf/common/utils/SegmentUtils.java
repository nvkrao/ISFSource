package isf.common.utils;

/**
 *
 * @author IN030223
 */
public class SegmentUtils {
    private  int row;
    private int col;
    private int band;
    private int bandTile;
    private int size;
    private String name;

    public int getRow() {
        return row;
    }

    public void setRow(int row) {
        this.row = row;
    }

    public int getCol() {
        return col;
    }

    public void setCol(int col) {
        this.col = col;
    }

    public int getBand() {
        return band;
    }

    public void setBand(int band) {
        this.band = band;
    }

    public int getBandTile() {
        return bandTile;
    }

    public void setBandTile(int bandTile) {
        this.bandTile = bandTile;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
